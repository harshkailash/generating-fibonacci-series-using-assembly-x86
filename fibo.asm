ASSUME CS:CODE, DS:DATA
DATA SEGMENT
FIB DW 11 DUP(?)
COUNT EQU 09H
DATA ENDS
CODE SEGMENT
START:
MOV AX,DATA
MOV DS,AX
XOR AX,AX
MOV CX,COUNT-1
MOV SI,OFFSET FIB
MOV AX,00H
MOV [SI],AX
MOV AX,01H
MOV [SI+1],AX
XOR AX,AX
LOOP1:
MOV AX,[SI]
MOV BX,[SI+1]
ADD SI,01H
ADD BX,AX
MOV [SI+1],BX
LOOP LOOP1
HLT
CODE ENDS
END START
